public class MathApi {
    public static void main(String[] args) {
      int a=56;
      int b=62;
      System.out.println(Math.abs(a-b));
      System.out.println(Math.floor(6.89));
      System.out.println(Math.ceil(5.87));
      System.out.println(Math.round(45.78));
      System.out.println(Math.sqrt(9.8));
      System.out.println(Math.pow(4, 3));
      System.out.println(Math.max(45, 78));
      System.out.println(Math.min(54,62));
      System.out.println(Math.random());
      
    }
}
